// Looping
// for merupakan kata kunci untuk melakukan perulangan
// blok kode yang terdapat dalam for akan selallu diulangi selaman kondisi for terpenuhi

// for(init statement; kondisi; post statement){
// 	// masukkan kode perulangan
// }

for(let i = 0; i <= 5; i++){
	console.log(i);
}

// While loop
// Merupakan versi peulangan yang lebih sederhana dibanding for loop
// Hanya terdapat kondisi perulangan tanpa ada init statement dan post statement

let counter = 1;
while (counter<= 10) {
	console.log(counter);
	counter++
}